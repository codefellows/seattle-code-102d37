console.log(sm + md);

// subtraction
console.log(lg - sm);
console.log(md - sm);

// multiplication
console.log(sm * sm);
console.log(md * md);

// division
console.log(lg / sm);
console.log(md / sm);



// ::::  LOGICAL  :::: //

